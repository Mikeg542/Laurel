if ( ! defined( 'ABSPATH' ) ) exit; // Exit if accessed directly

$template = get_option( 'template' );

switch( $template ) {
	case 'twentyeleven' :
		echo '</div></div>';
		break;
	case 'twentytwelve' :
		echo '</div></div>';
		break;
	case 'twentythirteen' :
		echo '</div></div>';
		break;
	case 'twentyfourteen' :
		echo '</div></div></div>';
		get_sidebar( 'content' );
		break;
	default :
		echo '</div>';
		if ( is_active_sidebar( 'shop-sidebar' ) ) {
			echo '<div id="secondary" class="widget-area col-sm-4" role="complementary">';
			dynamic_sidebar( 'shop-sidebar' );
			echo '</div>';
		}
		echo '</div></div></div>';
		break;
}